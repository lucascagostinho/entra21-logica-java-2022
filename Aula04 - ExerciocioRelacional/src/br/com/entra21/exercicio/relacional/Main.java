package br.com.entra21.exercicio.relacional;

public class Main {

	public static void main(String[] args) {
		
		int numA, numB;
		boolean resultado;
		
		numA = 11;
		numB = 20;
		
		resultado = numA > numB;
		System.out.println(resultado);
		
		resultado = numA >= numB;
		System.out.println(resultado);
		
		resultado = numA < numB;
		System.out.println(resultado);
		
		resultado = numA <= numB;
		System.out.println(resultado);
		
		
		

	}

}
